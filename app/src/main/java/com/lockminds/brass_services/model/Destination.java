package com.lockminds.brass_services.model;

public class Destination {
    String name, id;

    public Destination() {
    }

    public Destination(String name, String id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
